package net.tis.day01;

public class TestJumin4 {
	public static void main(String[] args) {
		String jumin="891214-1793485";		
		System.out.println("*****-"+jumin.substring((jumin.indexOf("-")+1)));
		
		String a="1200";
		int b = Integer.parseInt(a);
		System.out.println(a+98);
		System.out.println(b+98);		
		
		int x = 780000;
		String y = String.valueOf(x) ;
		System.out.println(x+98);
		System.out.println(y+98);
		
		//자바의 API java.lang팩키지 String클래스 method
		//static키워드가 있으면 클래스이름.필드
//		static	리턴값	필드명
//		 X		리턴값	필드명
		
	}//main end
}
